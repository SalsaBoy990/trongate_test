<p>Jazz is a music genre that originated in the African-American communities
    of New Orleans, Louisiana in the late 19th and early 20th centuries, with its roots in
    blues and ragtime. Since the 1920s Jazz Age, it has been recognized as a major form
    of musical expression in traditional and popular music. Jazz is characterized by
    swing and blue notes, complex chords, call and response vocals, polyrhythms and
    improvisation. Jazz has roots in European harmony and African rhythmic rituals.></p>

<p>As jazz spread around the world, it drew on national, regional, and local
    musical cultures, which gave rise to different styles. New Orleans jazz began in the
    early 1910s, combining earlier brass-band marches, French quadrilles, biguine,
    ragtime and blues with collective polyphonic improvisation. But jazz didn't begin
    as a single musical tradition in New Orleans or elsewhere. In the 1930s, arranged
    dance-oriented swing big bands, Kansas City jazz (a hard-swinging, bluesy,
    improvisational style), and gypsy jazz (a style that emphasized musette waltzes)
    were the prominent styles. Bebop emerged in the 1940s, shifting jazz from
    danceable popular music toward a more challenging "musician's music" which
    was played at faster tempos and used more chord-based improvisation. Cool jazz
    developed near the end of the 1940s, introducing calmer, smoother sounds and
    long, linear melodic lines.></p>

