<p>Classical music generally refers to the formal musical tradition of the
    Western world, considered to be distinct from Western folk music or popular
    music traditions. It is sometimes distinguished as Western classical music,
    as the term "classical music" may also refer to non-Western traditions which
    exhibit similar formal qualities. In addition to formality, classical music is
    often characterized by complexity in its musical form and harmonic
    organization, particularly with the use of polyphony. Since at least the
    9th-century it has been primarily a written tradition, spawning a sophisticated
    notational system, as well as accompanying literature in analytical, critical,
    historiographical, musicological and philosophical practices. A foundational
    component of Western Culture, classical music is frequently seen from the
    perspective of individual or groups of composers, whose compositions,
    personalities and beliefs have fundamentally shaped its history.</p>

<p>Rooted in the patronage of churches and royal courts in Western Europe,
    early medieval music was chiefly religious, monophonic and vocal, with the music
    of ancient Greece and Rome influencing its thought and theory. The earliest extant
    music manuscripts date from the Carolingian Empire (800–888), around the time
    which Western plainchant gradually unified into what is termed Gregorian chant.
    Musical centers existed at the Abbey of Saint Gall, the Abbey of Saint Martial
    and Saint Emmeram's Abbey, while the 11th-century saw the development of
    staff notation and increasing output from medieval music theorists. By the
    mid-12th century France became the major European musical center: the
    religious Notre-Dame school first fully explored organized rhythms and
    polyphony, while secular music flourished with the troubadour and trouvère
    traditions led by poet-musician nobles.</p>

